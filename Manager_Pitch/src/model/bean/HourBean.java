package model.bean;

public class HourBean {
	private int idHour;
	private String frameTime;
	
	public int getIdHour() {
		return idHour;
	}
	public void setIdHour(int idHour) {
		this.idHour = idHour;
	}
	public String getFrameTime() {
		return frameTime;
	}
	public void setFrameTime(String frameTime) {
		this.frameTime = frameTime;
	}
	
}
